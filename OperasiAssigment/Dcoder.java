    System.out.println("nilai a = " + a);
    
    // 2. Assignent operator pengurangan
    int b = 100;
    b -= 25; // b = b - 25;
    System.out.println("nilai b = " + b);
    
    // 3. Assignent operator perkalian
    int c = 100;
    c *= 25; // c = c x 20;
    System.out.println("nilai c = " + c);
    
    // 4. Assignment operator pembagian
    int d = 10;
    d /= 7; // d = d : 7;
    System.out.println("nilai d = " + d);
    
    // 5 Assignment operator modulus
